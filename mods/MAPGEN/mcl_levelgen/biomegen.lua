if core and core.get_meta then -- Mod environment.

local floor = math.floor
local fmod = math.fmod
local deserialize = core.deserialize
local serialize = core.serialize
local mod_storage = core.get_mod_storage ()
local v = vector.zero ()
local toquart = mcl_levelgen.toquart

function mcl_levelgen.get_biome (pos)
	local bx, by, bz
	bx = floor (pos.x / 16)
	by = floor (pos.y / 16)
	bz = floor (pos.z / 16)

	-- Do not attempt this if the engine might block to obtain
	-- metadata for this node.
	v.x = bx * 16
	v.y = by * 16
	v.z = bz * 16
	if not core.get_node_or_nil (v) then
		return nil
	end

	local meta = core.get_meta (v)
	local str = meta:get_string ("mcl_levelgen:biome_index")
	if not str or str == "" then
		-- Biome index was overwritten; load it from mod
		-- storage.
		str = mod_storage:get_string (table.concat ({
			tostring (bx), ",",
			tostring (by), ",",
			tostring (bz),
		}))

		if not str or str == "" then
			return nil
		end
		meta:set_string ("mcl_levelgen:biome_index", str)
	end

	local tbl = deserialize (str)
	local qx = toquart (floor (pos.x - v.x))
	local qy = toquart (floor (pos.y - v.y))
	local qz = toquart (floor (pos.z - v.z))
	return mcl_levelgen.index_biome_list (tbl, qx, qy, qz)
end

local function save_biome_index (pos, bx, by, bz, index)
	local str = serialize (index)
	local meta = core.get_meta (pos)
	meta:set_string ("mcl_levelgen:biome_index", str)
	mod_storage:set_string (table.concat ({
		tostring (bx), ",",
		tostring (by), ",",
		tostring (bz),
	}), str)
end

core.set_gen_notify ({ custom = true, }, nil, { "mcl_levelgen:biome_data", })

core.register_on_generated (function (minp, maxp, blockseed)
	local custom = core.get_mapgen_object ("gennotify").custom
	if not custom then
		return
	end
	local data = custom["mcl_levelgen:biome_data"]
	if not data then
		return
	end
	local x, y, z = minp.x, minp.y, minp.z
	local bx, by, bz = x / 16, y / 16, z / 16
	for pos, index in pairs (data) do
		local ix, iy, iz = pos.x, pos.y, pos.z
		pos.x = pos.x * 16 + x
		pos.y = pos.y * 16 + y
		pos.z = pos.z * 16 + z
		save_biome_index (pos, bx + ix, by + iy, bz + iz, index)
	end
end)

