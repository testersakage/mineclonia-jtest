------------------------------------------------------------------------
-- Biome generation.
------------------------------------------------------------------------

-- Number of quart positions per MapBlock.
local N = 4

local function index (x, y, z, dx, dy, dz)
	return x * dy * dz + y * dx + z + 1
end

if not core or core.save_gen_notify then -- Mapgen environment.

-- Return biomes in the table BIOMES for each position in the region
-- from X, Y, Z to X1, Y1, Z1, in PRESET, which must be Minecraft
-- block positions divided by 4.  The index of each position so
-- divided in this table is produced from the difference between each
-- such position and the supplied origin in X-major, Z-minor order.

function mcl_levelgen.generate_biomes (preset, biomes, x, y, z, x1, y1, z1)
	local dx = x1 - x + 1
	local dy = y1 - y + 1
	local dz = z1 - z + 1
	for xpos = x, x1 do
		for ypos = y, y1 do
			for zpos = z, z1 do
				local biome = preset:index_biomes (xpos, ypos, zpos)
				local idx = index (xpos - x, ypos - y, zpos - z,
						   dx, dy, dz)
				biomes[idx] = biome
			end
		end
	end
end

-- Likewise, but for a column of Minetest MapBlocks of dimensions W
-- and H at X, Y, Z (i.e., Minecraft X, Y, -Z - (W - 1) - 1).

function mcl_levelgen.generate_biomes_at_block (preset, biomes, x, y, z, w, h)
	local qx, qy, qz = x * N, y * N, z * N
	local qw, qh = w * N, h * N
	local qx2, qy2, qz2 = qx + qw - 1, qy + qh - 1, qz + qw - 1
	mcl_levelgen.generate_biomes (preset, biomes, qx, qy, -qz2 - 1,
				      qx2, qy2, -qz - 1)
end

-- Generate a run-length encoding indexed by MapBlock of a table of
-- biomes TABLE generated by mcl_levelgen.generate_biomes_at_block for
-- a column of MapBlocks W in width and H in height, assuming that
-- TABLE's coordinates have been translated into Minecraft's
-- coordinate system.  Return a table of node metadata values indexed
-- by suitably inverted relative MapBlock positions.

local insert = table.insert

function mcl_levelgen.encode_biomes (biomes, w, h, hash_for_testing)
	local qw = w * N
	local qh = h * N
	local max_index = index (qw - 1, qh - 1, qw - 1, qw, qh, qw)
	assert (#biomes == max_index)
	local x_stride = qw * qh
	local y_stride = qw
	local compressed = {}

	for x = 0, w - 1 do
		for y = 0, h - 1 do
			for z = 0, w - 1 do
				local qx, qy, qz = x * N, y * N, (w - z) * N
				local base = index (qx, qy, qz, qw, qh, qw)
				local list = {}
				local n, biome = 0, nil

				for ix = 0, N - 1 do
					local ybase = base
					for iy = 0, N - 1 do
						for iz = 1, N do
							local b = biomes[base - iz]
							if b == biome or not biome then
								n = n + 1
							else
								insert (list, n)
								insert (list, biome)
								n = 1
							end
							biome = b
						end
						ybase = ybase + y_stride
					end
					base = base + x_stride
				end
				if biome ~= nil then
					insert (list, n)
					insert (list, biome)
				end
				if not hash_for_testing then
					compressed[{ x = x, y = y, z = z, }] = list
				else
					compressed[x * h * w + y * w + z + 1] = list
				end
			end
		end
	end
	return compressed
end

end

function mcl_levelgen.index_biome_list (list, qx, qy, qz)
	assert (qx < N and qy < N and qz < N)
	local i, idx = 1, qx * N * N + qy * N + qz + 1
	local biome
	repeat
		idx = idx - list[i]
		biome = list[i + 1]
		i = i + 2
	until idx <= 0
	return biome
end

